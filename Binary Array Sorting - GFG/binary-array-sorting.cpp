class Solution{
  public:
    
    // A[]: input array
    // N: input array
    //Function to sort the binary array.
    void binSort(int A[], int n)
    {
       int low = 0 , high = n-1;
       while(low < high){
           while(low < high && A[low] == 0){
               low++;
           }
           while(low < high && A[high] == 1){
               high--;
           }
           if(low < high){
               swap(A[low],A[high]);
               low++;
               high--;
           }
       }
    }
};

